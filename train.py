def merge_and_process_doccano(file_paths):
    all_tokens = []
    all_ner_ids = []
    all_texts = []
    all_ids = []

    seen_texts = set()

    print(f"🔄 Processing {len(file_paths)} file(s)...")

    for path in file_paths:
        if not os.path.exists(path):
            print(f"⚠️ Missing file: {path}")
            continue

        with open(path, "r", encoding="utf-8") as f:
            for line_no, line in enumerate(f, start=1):
                line = line.strip()
                if not line:
                    continue

                try:
                    record = json.loads(line)

                    # Doccano JSONL thường có: id, text, label, Comments
                    text = record.get("text", "")
                    spans = record.get("label", []) or []
                    rid = record.get("id", None)

                    text = text.strip()
                    if not text:
                        continue

                    # 1) dedupe by text
                    if text in seen_texts:
                        continue
                    seen_texts.add(text)

                    # 2) char-level BIO map
                    char_labels = ["O"] * len(text)

                    # --- xử lý overlap: ưu tiên span dài hơn trước,
                    # và chỉ ghi nếu vị trí đó còn là O (để tránh chồng nhãn)
                    # spans: [start, end, label_name]
                    clean_spans = []
                    for sp in spans:
                        if not isinstance(sp, (list, tuple)) or len(sp) < 3:
                            continue
                        start, end, label_name = sp[0], sp[1], sp[2]
                        if not isinstance(start, int) or not isinstance(end, int):
                            continue
                        if start < 0 or end <= start:
                            continue
                        if start >= len(text):
                            continue
                        label_name = str(label_name).strip()

                        # chỉ nhận label nằm trong ENTITY_LABELS
                        if label_name not in ENTITY_LABELS:
                            continue

                        clean_spans.append((start, min(end, len(text)), label_name))

                    # sort by length desc
                    clean_spans.sort(key=lambda x: (x[1]-x[0]), reverse=True)

                    for start, end, label_name in clean_spans:
                        b_tag = f"B-{label_name}"
                        i_tag = f"I-{label_name}"

                        # nếu chỗ start đã bị gán label khác, bỏ qua span này
                        if char_labels[start] != "O":
                            continue

                        # gán B
                        if b_tag in label2id:
                            char_labels[start] = b_tag

                        # gán I cho phần còn lại nếu còn O
                        for i in range(start + 1, end):
                            if char_labels[i] == "O" and i_tag in label2id:
                                char_labels[i] = i_tag

                    # 3) tokenize + lấy label theo ký tự đầu token
                    tokens = []
                    ner_ids = []

                    for m in TOKEN_RE.finditer(text):
                        tok = m.group()
                        s = m.start()

                        # label token = label tại ký tự đầu token
                        tok_label = char_labels[s] if s < len(char_labels) else "O"
                        tok_id = label2id.get(tok_label, 0)

                        tokens.append(tok)
                        ner_ids.append(tok_id)

                    if tokens:
                        all_ids.append(rid)
                        all_texts.append(text)
                        all_tokens.append(tokens)
                        all_ner_ids.append(ner_ids)

                except Exception as e:
                    print(f"⚠️ Error in {os.path.basename(path)} line {line_no}: {e}")
                    continue

    return all_ids, all_texts, all_tokens, all_ner_ids


