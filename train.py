def compute_metrics(p):
    predictions, labels = p
    predictions = np.argmax(predictions, axis=2)

    true_predictions = []
    true_labels = []

    for pred_seq, lab_seq in zip(predictions, labels):
        cur_pred = []
        cur_lab = []
        for p_id, l_id in zip(pred_seq, lab_seq):
            if l_id == -100:
                continue
            cur_pred.append(id2label.get(int(p_id), "O"))
            cur_lab.append(id2label.get(int(l_id), "O"))
        true_predictions.append(cur_pred)
        true_labels.append(cur_lab)

    results = metric.compute(predictions=true_predictions, references=true_labels)
    return {
        "precision": results["overall_precision"],
        "recall": results["overall_recall"],
        "f1": results["overall_f1"],
        "accuracy": results["overall_accuracy"],
    }

