def find_span_sequential(text, phrase, used_until=0):
    text_low = text.lower()
    phrase_low = phrase.lower().strip()

    if not phrase_low:
        return None, None

    start = text_low.find(phrase_low, used_until)
    if start == -1:
        start = text_low.find(phrase_low)

    if start == -1:
        return None, None

    end = start + len(phrase_low)
    return start, end

