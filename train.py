def clean_tokens(token_list):
    """
    Tách dấu câu ra khỏi token.
    Ví dụ:
    "đẹp," -> ["đẹp", ","]
    "tiền." -> ["tiền", "."]
    """

    new_tokens = []

    for token in token_list:

        # regex tách word và punctuation
        parts = re.findall(r'\w+|[^\w\s]', token, flags=re.UNICODE)

        new_tokens.extend(parts)

    return new_tokens

