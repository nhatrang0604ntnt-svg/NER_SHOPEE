def predict_to_doccano_record(text, rec_id, min_score=0.3):
    try:
        # cắt bớt text quá dài cho an toàn
        text = text[:600]
        results = ner_pipeline(text)
    except Exception as e:
        print(f"⚠️ Lỗi dòng {rec_id}: {e}")
        return {"id": rec_id, "text": text, "label": [], "Comments": []}

    labels = []
    used_until = 0

    for r in results:
        try:
            score = float(r.get("score", 0))
            if score < min_score:
                continue

            ent = r.get("entity_group", r.get("entity", "UNK"))
            word = clean_pred_word(r.get("word", ""))

            start = r.get("start", None)
            end = r.get("end", None)

            if start is None or end is None:
                start, end = find_span_sequential(text, word, used_until=used_until)

            if start is None or end is None:
                continue
            if not isinstance(start, int) or not isinstance(end, int):
                continue
            if start < 0 or end <= start or end > len(text):
                continue

            labels.append([start, end, ent])
            used_until = end

        except:
            continue

    # sort + dedup
    labels = sorted(labels, key=lambda x: (x[0], x[1], x[2]))
    dedup = []
    seen = set()
    for x in labels:
        key = (x[0], x[1], x[2])
        if key not in seen:
            dedup.append(x)
            seen.add(key)

    return {
        "id": rec_id,
        "text": text,
        "label": dedup,
        "Comments": []
    }

