MAX_LEN = 256

def tokenize_and_align_labels(examples):
    """
    examples["tokens"]  : List[List[str]]
    examples["ner_ids"] : List[List[int]]
    """
    # --- sanity check (debug) ---
    # Nếu bị lỗi, in ra 1 mẫu để biết sai cấu trúc
    if "tokens" not in examples or "ner_ids" not in examples:
        raise KeyError("raw_datasets phải có cột 'tokens' và 'ner_ids'")

    # Tokenize theo word-level input
    tokenized_inputs = tokenizer(
        examples["tokens"],
        truncation=True,
        is_split_into_words=True,
        max_length=MAX_LEN,
        padding="max_length"
    )

    aligned_labels = []

    if tokenizer.is_fast:
        # FAST: dùng word_ids
        for i, word_labels in enumerate(examples["ner_ids"]):
            word_ids = tokenized_inputs.word_ids(batch_index=i)

            label_ids = []
            prev_word_idx = None

            for word_idx in word_ids:
                if word_idx is None:
                    label_ids.append(-100)
                elif word_idx != prev_word_idx:
                    # label cho token đầu của 1 word
                    if word_idx < len(word_labels):
                        label_ids.append(word_labels[word_idx])
                    else:
                        label_ids.append(-100)
                else:
                    # token tiếp theo của cùng word -> ignore
                    label_ids.append(-100)
                prev_word_idx = word_idx

            aligned_labels.append(label_ids)

    else:
        # SLOW: tokenize từng word để biết số sub_tokens
        for i, word_labels in enumerate(examples["ner_ids"]):
            label_ids = []

            # (a) add special token đầu
            label_ids.append(-100)  # <s>

            # (b) gán label cho subword đầu, subword sau -100
            for w_idx, word in enumerate(examples["tokens"][i]):
                sub_tokens = tokenizer.tokenize(word)
                if not sub_tokens:
                    continue

                # token đầu
                if w_idx < len(word_labels):
                    label_ids.append(word_labels[w_idx])
                else:
                    label_ids.append(-100)

                # sub token còn lại
                if len(sub_tokens) > 1:
                    label_ids.extend([-100] * (len(sub_tokens) - 1))

            # (c) add special token cuối
            label_ids.append(-100)  # </s>

            # (d) pad/crop đúng MAX_LEN
            if len(label_ids) > MAX_LEN:
                label_ids = label_ids[:MAX_LEN]
            else:
                label_ids = label_ids + [-100] * (MAX_LEN - len(label_ids))

            aligned_labels.append(label_ids)

    tokenized_inputs["labels"] = aligned_labels
    return tokenized_inputs


