def predict_signal_columns(text, min_score=0.3, max_len=600):
    text = str(text).strip()

    empty_result = {EXPORT_COLUMN_MAP.get(lb, lb): [] for lb in TARGET_LABELS}

    if not text:
        return empty_result

    text = text[:max_len]

    try:
        results = ner_pipeline(text)
    except Exception as e:
        print(f"⚠️ Lỗi predict: {e}")
        return empty_result

    signal_dict = {EXPORT_COLUMN_MAP.get(lb, lb): [] for lb in TARGET_LABELS}

    for r in results:
        try:
            score = float(r.get("score", 0))
            if score < min_score:
                continue

            ent = r.get("entity_group", r.get("entity", "UNK"))

            # fix BIO label nếu có
            if isinstance(ent, str) and (ent.startswith("B-") or ent.startswith("I-")):
                ent = ent[2:]

            word = clean_pred_word(r.get("word", ""))

            if not word:
                continue

            if ent in TARGET_LABELS:
                out_col = EXPORT_COLUMN_MAP.get(ent, ent)
                signal_dict[out_col].append(word)

        except:
            continue

    for k in signal_dict:
        signal_dict[k] = dedup_keep_order(signal_dict[k])

    return signal_dict

