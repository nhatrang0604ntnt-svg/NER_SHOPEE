def pad_or_truncate_ner(example, target_len=TARGET_LEN):
    input_ids = example["input_ids"]
    labels = example["labels"]
    attention_mask = example.get("attention_mask", [1] * len(input_ids))

    # nếu dài hơn target_len thì cắt
    if len(input_ids) > target_len:
        input_ids = input_ids[:target_len]
        labels = labels[:target_len]
        attention_mask = attention_mask[:target_len]

    # nếu ngắn hơn target_len thì pad
    elif len(input_ids) < target_len:
        need = target_len - len(input_ids)
        input_ids = input_ids + [PAD_ID] * need
        labels = labels + [-100] * need
        attention_mask = attention_mask + [0] * need

    return {
        "input_ids": input_ids,
        "labels": labels,
        "attention_mask": attention_mask
    }

