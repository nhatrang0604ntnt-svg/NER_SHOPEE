import re

def clean_and_split_punctuation(text):
    """
    Chuẩn hóa text review Shopee:
    - tách dấu câu ra khỏi từ
    - xóa khoảng trắng thừa
    - giữ nguyên chữ tiếng Việt
    """

    if not isinstance(text, str):
        return ""

    # 1. chuẩn hóa khoảng trắng
    text = re.sub(r"\s+", " ", text).strip()

    # 2. tách dấu câu phổ biến
    text = re.sub(r'([.,!?;:()"\'])', r' \1 ', text)

    # 3. xử lý dấu ...
    text = re.sub(r'\.{2,}', ' ... ', text)

    # 4. chuẩn hóa khoảng trắng lần nữa
    text = re.sub(r"\s+", " ", text).strip()

    return text


